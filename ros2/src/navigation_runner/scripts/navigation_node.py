#!/usr/bin/env python3
# =============================================================================
# Navigation Node Main 导航主程序入口
# =============================================================================
# 该文件是导航系统的入口点，负责初始化 ROS2 节点并启动导航功能
# 启动命令: ros2 run navigation_runner navigation_node.py
# 或: ros2 launch navigation_runner navigation.launch.py
# =============================================================================

import rclpy
from rclpy.executors import MultiThreadedExecutor
from navigation import Navigation
from hydra import compose, initialize

# 配置文件相对路径
# 该路径相对于 navigation_node.py 的位置
# Hydra 将从此路径加载 train.yaml 配置文件
RELATIVE_FILE_PATH = "cfg"


def main(args=None):
    """
    导航主函数
    
    参数:
        args: 命令行参数 (由 ROS2 传递)
    
    功能:
        1. 初始化 ROS2 环境
        2. 使用 Hydra 加载配置文件
        3. 创建 Navigation 节点
        4. 启动多线程执行器
        5. 进入主循环处理回调
        6. 清理资源并关闭
    """
    # =========================================================================
    # 初始化 ROS2
    # =========================================================================
    # 初始化 rclpy 库，准备创建节点
    rclpy.init(args=args)

    # =========================================================================
    # 使用 Hydra 加载配置
    # =========================================================================
    # 配置文件结构 (cfg/):
    #   train.yaml          # 主配置文件
    #   ├── sim.yaml        # 仿真参数
    #   ├── drone.yaml      # 无人机参数
    #   ├── ppo.yaml        # PPO算法参数
    #   └── task.yaml       # 任务参数
    #
    # 配置内容:
    #   - algo: 算法参数 (PPO网络结构、学习率等)
    #   - sim: 仿真参数 (物理引擎、场景等)
    #   - sensor: 传感器参数 (LiDAR分辨率、范围等)
    #   - task: 任务参数 (目标点、奖励函数等)
    #   - device: 计算设备 (cuda/cpu)
    with initialize(config_path=RELATIVE_FILE_PATH, version_base=None):
        # compose() 组合所有配置文件
        cfg = compose(config_name="train")

    # =========================================================================
    # 创建导航节点
    # =========================================================================
    # Navigation 类继承自 rclpy.node.Node
    # 负责:
    #   - 加载 PPO 策略模型
    #   - 创建订阅者和发布者
    #   - 实现控制逻辑
    #   - 处理传感器数据
    nav = Navigation(cfg)

    # =========================================================================
    # 启动导航功能
    # =========================================================================
    # run() 方法创建定时器，启动各个回调函数:
    #   - raycast_callback: 20Hz，获取 LiDAR 数据
    #   - dynamic_obstacle_callback: 20Hz，获取动态障碍物
    #   - control_callback: 20Hz，主控制循环
    #   - goal_vis_callback: 20Hz，目标点可视化
    nav.run()

    # =========================================================================
    # 使用多线程执行器
    # =========================================================================
    # MultiThreadedExecutor 允许并发执行多个回调函数
    # 这对于导航系统很重要，因为:
    #   - 传感器回调需要及时处理 (避免数据丢失)
    #   - 控制循环需要稳定频率 (20Hz)
    #   - 服务调用可能阻塞 (等待响应)
    #
    # 线程分配:
    #   - 主线程: 处理控制循环 (时间敏感)
    #   - 辅助线程: 处理传感器数据和服务调用
    #
    # 注意: Navigation 类中使用了 MutuallyExclusiveCallbackGroup
    # 确保同一组内的回调不会并发执行 (避免竞态条件)
    executor = MultiThreadedExecutor()
    executor.add_node(nav)
    
    # spin() 开始处理回调，阻塞直到节点关闭
    # 内部循环:
    #   1. 等待消息/服务请求/定时器事件
    #   2. 调用对应的回调函数
    #   3. 重复直到 rclpy.shutdown()
    executor.spin()
    
    # 单线程执行器的替代方案 (不推荐用于实时控制):
    # rclpy.spin(nav)

    # =========================================================================
    # 清理资源
    # =========================================================================
    # destroy_node() 清理节点资源:
    #   - 取消所有定时器
    #   - 销毁所有发布者和订阅者
    #   - 关闭所有服务客户端
    nav.destroy_node()
    
    rclpy.shutdown()


if __name__ == "__main__":
    # 当直接运行该脚本时调用 main()
    # 当作为模块导入时不执行
    main()
